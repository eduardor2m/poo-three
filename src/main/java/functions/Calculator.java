package functions;

public class Calculator {
    public float sum(float numberOne, float numberTwo) {
        return numberOne + numberTwo;
    }

    public float subtract(float numberOne, float numberTwo) {
        return numberOne - numberTwo;
    }

    public float divide(float numberOne, float numberTwo) {
        return numberOne / numberTwo;
    }

    public float multiply(float numberOne, float numberTwo) {
        return numberOne * numberTwo;
    }
}
